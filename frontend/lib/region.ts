export interface RegionInfo {
  sidoName: string     // mtrpl_lcgv_nm
  sigunguName: string  // basic_lcgv_nm (없을 수도 있음)
}

/**
 * 10자리 행정동 코드 → 시도명 + 시군구명
 */
export function getRegionFromStdgCd(stdgCd: string): RegionInfo {
  if (!stdgCd || stdgCd.length < 2) {
    return { sidoName: '', sigunguName: '' }
  }
  const sidoCode = stdgCd.substring(0, 2)
  const sigunguCode = stdgCd.substring(0, 5)

  return {
    sidoName: SIDO_MAP[sidoCode] ?? '',
    sigunguName: SIGUNGU_MAP[sigunguCode] ?? '',
  }
}

/**
 * stdgCd 앞 7자리 → 버스 API localgvrncd 형식 (7자리 + 000)
 * 예: "4131053000" → "4131000000" (성남시 단위)
 */
export function toLocalgvrncd(stdgCd: string): string {
  if (!stdgCd || stdgCd.length < 5) return ''
  return stdgCd.substring(0, 5).padEnd(10, '0')
}
